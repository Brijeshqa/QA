
public class Operator {
	public static void main(String args[]){
		int x=10;
		int y=5;
		int z=20;
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y);
		System.out.println(x%y);
		System.out.println(y/x);
		System.out.println(10>>2);
		System.out.println(10<<2);
		System.out.println(20>>2);
		System.out.println(20>>>2);
		System.out.println(-20>>2);
		System.out.println(-20>>>2);
		System.out.println(x<y||x<z);
		System.out.println(x<y&&x<z);
	}

}
